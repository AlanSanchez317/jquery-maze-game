    function applyWaveAnimation() {
        $('#title-wave span').each(function(index) {
            var delay = index;
            var position = 0;
            setInterval(() => {
                $(this).css('transform', 'translateY(' + Math.sin(position) * 10 + 'px)');
                position += 0.1;
                if (position > 2 * Math.PI) {
                    position = 0;
                }
            }, delay);
        });
    }
